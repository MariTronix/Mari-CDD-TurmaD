#Faça um programa para ler o nome de uma pessoa, sua idade e seu salário e mostre
#essas informações na tela.

#variavel de nome
nome = input("Digite seu nome: ")

#varialvel com idade, int é numero interio
idade = int(input("Digite sua idade"))

#variavel de salario com float
salario = float(input("Digite o salario"))

#saida de resultado
print(f"Nome: {nome}, Idade: {idade} e Salario: {salario}")